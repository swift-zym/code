{
    opter.push('#');      //'#'opter
    int counter = 0;      //counter
    char c = mygetchar();
    while (c != '#' || opter.top() != '#')   //d
    {
        if (isdigit(c))   //c'0'~'9'
        {
            if (counter == 1)   //counter==112*121212
            {
                int t = opval.top();
                opval.pop();
                opval.push(t * 10 + (c - '0'));
                counter = 1;
            }
            else
            {
                opval.push(c - '0');     //copval
                counter++;
            }
            c = mygetchar();
        }
        else
        {
            counter = 0;   //counter
            switch (getPriority(opter.top(), c))   //opterc'>''<''='
            {
                case '<':               //<copter
                    opter.push(c);
                    c = mygetchar();
                    break;
                case '=':               //=opter
                    opter.pop();
                    c = mygetchar();
                    break;
                case '>':               //>
                    char theta = opter.top();
                    opter.pop();
                    int a = opval.top();
                    opval.pop();
                    int b = opval.top();
                    opval.pop();
                    opval.push(calculate(b, theta, a));
            }
        }
    }
    return opval.top();   //opval
}
